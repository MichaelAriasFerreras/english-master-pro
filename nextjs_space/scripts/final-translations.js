async function updateAllRemaining() {
  let updated = 0;
  let errors = 0;
  const total = Object.keys(translations).length;
  
  console.log(`🚀 Actualizando ${total} verbos restantes...\n`);
  
  for (const [infinitive, data] of Object.entries(translations)) {
    try {
      const verb = await prisma.verb.findFirst({ where: { infinitive } });
      
      if (verb) {
        await prisma.verb.update({
          where: { id: verb.id },
          data: {
            spanishTranslation: data[0],
            examples: [data[1]],
            spanishExamples: [data[2]]
          }
        });
        updated++;
        if (updated % 50 === 0) {
          console.log(`✅ Progreso: ${updated}/${total}`);
        }
      }
    } catch (error) {
      errors++;
      console.error(`❌ Error con "${infinitive}"`);
    }
  }
  
  console.log(`\n🎉 Completado: ${updated} verbos actualizados`);
  console.log(`Errores: ${errors}`);
  await prisma.$disconnect();
}

