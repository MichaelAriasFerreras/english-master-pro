// Función para generar ejemplos automáticamente
function generateExamples(verb: string, level: string): { english: string, spanish: string }[] {
  const examples = [];
  
  // Ejemplo básico de presente
  examples.push({
    english: `I ${verb} every day`,
    spanish: `Yo ${translations[verb] || verb} todos los días`
  });
  
  // Ejemplo de pasado
  const pastForm = verb.endsWith('e') ? `${verb}d` : `${verb}ed`;
  examples.push({
    english: `Yesterday I ${pastForm}`,
    spanish: `Ayer yo ${translations[verb] || verb}í`
  });
  
  // Ejemplo de futuro
  examples.push({
    english: `Tomorrow I will ${verb}`,
    spanish: `Mañana yo voy a ${translations[verb] || verb}`
  });
  
  return examples;
}

async function main() {
  const allVerbs = await prisma.verb.findMany();
  
  console.log(`Total de verbos en base de datos: ${allVerbs.length}`);
  console.log(`Comenzando traducción y agregación de ejemplos...`);
  
  let translated = 0;
  let failed = 0;
  
  for (const verb of allVerbs) {
    try {
      const translation = translations[verb.infinitive.toLowerCase()] || verb.infinitive;
      const examples = generateExamples(verb.infinitive, verb.level);
      
      await prisma.verb.update({
        where: { id: verb.id },
        data: {
          translation,
          exampleEn1: examples[0]?.english || '',
          exampleEs1: examples[0]?.spanish || '',
          exampleEn2: examples[1]?.english || '',
          exampleEs2: examples[1]?.spanish || '',
          exampleEn3: examples[2]?.english || '',
          exampleEs3: examples[2]?.spanish || ''
        }
      });
      
      translated++;
      
      if (translated % 50 === 0) {
        console.log(`Progreso: ${translated}/${allVerbs.length} verbos procesados`);
      }
    } catch (error) {
      console.error(`Error procesando verbo ${verb.infinitive}:`, error);
      failed++;
    }
  }
  
  console.log('\n=== RESUMEN ===');
  console.log(`Total procesados: ${translated}`);
  console.log(`Errores: ${failed}`);
  console.log(`✓ Todos los verbos han sido traducidos y tienen ejemplos`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
